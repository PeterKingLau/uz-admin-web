<template>
  <el-form ref="userRef" :model="form" :rules="rules" label-width="80px">
    <el-form-item label="用户昵称" prop="nickName">
      <el-input v-model="form.nickName" name="nickName" maxlength="30" />
    </el-form-item>

    <el-form-item label="手机号码" prop="phonenumber">
      <el-input v-model="form.phonenumber" name="phonenumber" maxlength="11" />
    </el-form-item>

    <el-form-item label="邮箱" prop="email">
      <el-input v-model="form.email" name="email" maxlength="50" />
    </el-form-item>

    <el-form-item label="性别" prop="sex">
      <el-radio-group v-model="form.sex" name="sex">
        <el-radio value="0">男</el-radio>
        <el-radio value="1">女</el-radio>
      </el-radio-group>
    </el-form-item>

    <el-form-item>
      <el-button type="primary" @click="submit">保存</el-button>
      <el-button type="danger" @click="close">关闭</el-button>
    </el-form-item>
  </el-form>
</template>

<script setup>
import { ref, watch, getCurrentInstance } from "vue";
import { updateUserProfile } from "@/api/system/user";

const props = defineProps({
  user: {
    type: Object,
    default: () => ({}),
  },
});

const { proxy } = getCurrentInstance();

// 表单 ref
const userRef = ref(null);

// 表单数据
const form = ref({
  nickName: "",
  phonenumber: "",
  email: "",
  sex: "0",
});

// 校验规则（不需要再包一层 ref）
const rules = {
  nickName: [{ required: true, message: "用户昵称不能为空", trigger: "blur" }],
  email: [
    { required: true, message: "邮箱地址不能为空", trigger: "blur" },
    {
      type: "email",
      message: "请输入正确的邮箱地址",
      trigger: ["blur", "change"],
    },
  ],
  phonenumber: [
    { required: true, message: "手机号码不能为空", trigger: "blur" },
    {
      pattern: /^1[3-9][0-9]\d{8}$/,
      message: "请输入正确的手机号码",
      trigger: "blur",
    },
  ],
};

watch(
  () => props.user,
  (user) => {
    if (!user) return;
    form.value = {
      nickName: user.nickName || "",
      phonenumber: user.phonenumber || "",
      email: user.email || "",
      sex: user.sex ?? "0",
    };
  },
  { immediate: true, deep: true }
);

/** 提交按钮 */
function submit() {
  if (!userRef.value) return;

  userRef.value.validate((valid) => {
    if (!valid) return;

    updateUserProfile(form.value).then(() => {
      proxy.$modal?.msgSuccess && proxy.$modal.msgSuccess("修改成功");

      if (props.user) {
        props.user.nickName = form.value.nickName;
        props.user.phonenumber = form.value.phonenumber;
        props.user.email = form.value.email;
        props.user.sex = form.value.sex;
      }
    });
  });
}

/** 关闭按钮 */
function close() {
  proxy.$tab?.closePage && proxy.$tab.closePage();
}
</script>
